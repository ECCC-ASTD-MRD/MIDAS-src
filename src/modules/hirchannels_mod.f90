  integer function hir_get_nchan_selected(CINST)
    implicit none
    character (len=*),intent(in) :: CINST


    select case(trim(CINST))
    case("AIRS","airs")
       hir_get_nchan_selected= AIRSSNCH
    case("IASI","iasi")
       hir_get_nchan_selected= IASISNCH
    case("CRIS","cris","CrIS")
       hir_get_nchan_selected = CRISSNCH
    case default
       Write(*,*) "Unknown instrument! ",CINST
       call utl_abort("hir_get_nchan_selected from  hirchannels module")
    end select


  end function hir_get_nchan_selected


  integer function hir_get_chindx_fr_chn(CINST,ichan)
    implicit none
    character (len=*),intent(in) :: CINST
    integer,intent(in) :: ichan
    !*************************************************
    integer :: indx

    if (ichan<1) then
       Write(*,*) "Channel number ",ichan," should be a positive integer !"
       call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
    endif

    select case(trim(CINST))
    case("AIRS","airs")
       if (ichan>AIRSRNCH) then
          Write(*,*) "Channel ",ichan," exceeds ",AIRSRNCH," for the AIRS instrument"
          call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
       endif
       indx=AIRSSCH(ichan)
       if (indx/=-1) then
          hir_get_chindx_fr_chn = indx
       else
          Write(*,*) "Channel ",ichan," is not selected for the AIRS instrument"
          call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
       endif
    case("IASI","iasi")
       if (ichan>IASIRNCH) then
          Write(*,*) "Channel ",ichan," exceeds ",IASIRNCH," for the IASI instrument"
          call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
       endif
       indx=IASISCH(ichan)
       if (indx/=-1) then
          hir_get_chindx_fr_chn = indx
       else
          Write(*,*) "Channel ",ichan," is not selected for the IASI instrument"
          call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
       endif
    case("CRIS","cris","CrIS")
       if (ichan>CRISRNCH) then
          Write(*,*) "Channel ",ichan," exceeds ",CRISRNCH," for the CRIS instrument"
          call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
       endif
       indx=CRISSCH(ichan)
       if (indx/=-1) then
          hir_get_chindx_fr_chn = indx
       else
          Write(*,*) "Channel ",ichan," is not selected for the CRIS instrument"
          call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
       endif
    case default
       Write(*,*) "Unknown instrument! ",CINST
       call utl_abort("hir_get_chindx_fr_chn from  hirchannels module")
    end select


  end function hir_get_chindx_fr_chn


  subroutine hir_set_assim_chan(CINST,iselec)
    implicit none
    character (len=*),intent(in) :: CINST
    integer,intent(in) :: iselec(:)
    !*************************************************
    integer :: nch,i
    integer ,pointer :: pt(:)
    !***************************

    nch=size(iselec)

    if (nch<1) then
       Write(*,*) "Empty channel selection !"
       call utl_abort("hir_set_assim_chan from  hirchannels module")
    endif

    if (nch/=hir_get_nchan_selected(CINST)) then
       Write(*,*) "Wrong number of channels",nch
       Write(*,*) "Should be ",hir_get_nchan_selected(CINST)
       call utl_abort("hir_set_assim_chan from  hirchannels module")
    endif

    select case(trim(CINST))
    case("AIRS","airs")
       pt=>airs_assim
    case("IASI","iasi")
       pt=>iasi_assim
    case("CRIS","cris","CrIS")
       pt=>cris_assim
    case default
       Write(*,*) "Unknown instrument! ",CINST
       call utl_abort("set_assim_chan from  hirchannels module")
    end select

    do i=1,nch
       if (iselec(i)==0 .or. iselec(i)==1) then
          pt(i)=iselec(i)
       else
          Write(*,*) "Invalid selection flag ",iselec(i),i,cinst
          call utl_abort("hir_set_assim_chan from  hirchannels module")
       endif
    enddo

    nullify(pt)

  end subroutine hir_set_assim_chan


  integer function hir_get_assim_chan(CINST,ichan)
    implicit none
    character (len=*),intent(in) :: CINST
    integer,intent(in) :: ichan
    !*******************************
    integer ,pointer :: pt(:)

    select case(trim(CINST))
    case("AIRS","airs")
       pt=>airs_assim
    case("IASI","iasi")
       pt=>iasi_assim
    case("CRIS","cris","CrIS")
       pt=>cris_assim
    case default
       Write(*,*) "Unknown instrument! ",CINST
       call utl_abort("hir_get_assim_chan from  hirchannels module")
    end select

    if (ichan<1 .or. ichan>hir_get_nchan_selected(CINST)) then
       Write(*,*) "Invalid channel index",ichan
       call utl_abort("hir_get_assim_chan from  hirchannels module")
    endif

    hir_get_assim_chan = pt(ichan)

    nullify(pt)

  end function hir_get_assim_chan

